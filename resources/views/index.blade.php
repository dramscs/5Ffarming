                            <h2>5Ffarming Background</h2>
                        </div>
                        <div class="text para-container">
                            <p>Agriculture has always been challenged by the vagaries of the weather that make development of climate resilient crop varieties an immediate need. This will help India in increasing sustainable productionof high quality Food, Feed, Fiber, Fuel & Fine chemicals (5F)</p>

                            <p>Despite all the technological advancements, market linkages, and forward market integration, agriculture is yet to gain the status of a profitable business for majority of the small and marginal farmers due to the engagement of the middlemen poor technical awareness, dearth ofproficient minds and meager attention paid to develop value added crop varieties for the secondary market</p>

                            <p>Gene revolution has no doubt played an important role not only in improving the productivity but also in increasing the efficiency of resources like land, water, labour and other vital inputs for sustainableprofitability. Some key considerations for ‘Gene revolution for profitable agriculture is:</p>

                            <ul class="org-background-list">
                                <li>Gene revolution for biotic and abiotic resistance</li>
                                <li><strong>Value addition through crop improvement</strong> and making the produce more appropriate for the secondary farming</li>
                                <li><strong>Teaching /training the farmers/entrepreneurs for improved technologies</strong> for better crop management and environment resilience</li>
                                <li><strong>Attracting young and talented minds to agriculture</strong> as a profession and train them in making farming more profitable </li>
                                <li>Farm automation for small farmers from planting to harvesting </li>
                            </ul>

                            <p>In this Conference, knowledgeable keynote and invited professional practioners of agricultural sciences will review the latestprogresses of enabling technologies for gene revolution thatcan add value to the product, increase profitability to the farmers and attract youth and talentpool to join the work force in agriculture</p>

                            <p>Experts will also deliberate on the challenges of marketing faced by the growers, and formulate strategies to reinforce policies supporting development of agriculture in India</p>

                            <p>This event will also provide a networking opportunity for theProfessionals, Scientists, Industry Leaders, Policy-Makers, Farmers, Students and other stakeholders and a platform to learn from each other.</p>
